import Foundation

class Service: Model {

    var id: NSNumber!
    var name: String!

    override class func mapping() -> RKObjectMapping {
        let mapping = RKObjectMapping(forClass: self)
        mapping.addAttributeMappingsFromDictionary([
            "id": "id",
            "name": "name"
            ])
        return mapping
    }
}
